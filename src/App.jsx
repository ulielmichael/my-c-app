function CodeBlock({ code }) {
  const [copied, setCopied] = useState(false);
  const copy = () => {
    navigator.clipboard.writeText(code).catch(() => {});
    setCopied(true);
    setTimeout(() => setCopied(false), 1500);
  };

  const highlight = (line) =>
    line
      .replace(/(#include|#define|#ifdef|#ifndef|#endif|int|float|char|double|void|return|if|else|while|for|do|switch|case|break|default|typedef|struct|union|const|unsigned|long|NULL|static)\b/g, '<k>$1</k>')
      .replace(/("(?:[^"\\]|\\.)*")/g, '<s>$1</s>')
      .replace(/(\/\/.*$)/g, '<c>$1</c>')
      .replace(/\b(\d+\.?\d*[fFlLuU]*)\b/g, '<n>$1</n>')
      .replace(/<k>/g, '<span style="color:#88c0d0;font-weight:600">')
      .replace(/<\/k>/g, '</span>')
      .replace(/<s>/g, '<span style="color:#a3be8c">')
      .replace(/<\/s>/g, '</span>')
      .replace(/<c>/g, '<span style="color:#616e88;font-style:italic">')
      .replace(/<\/c>/g, '</span>')
      .replace(/<n>/g, '<span style="color:#d08770">')
      .replace(/<\/n>/g, '</span>');

  return (
    <div style={{ borderRadius: 12, overflow: "hidden", border: `1px solid #1e3a5f` }}>
      <div style={{ background: "#0a1628", padding: "8px 16px", display: "flex", justifyContent: "space-between", alignItems: "center", borderBottom: "1px solid #1e3a5f" }}>
        <span style={{ color: C.muted, fontSize: 11, fontFamily: "monospace", letterSpacing: 2 }}>C SOURCE</span>
        <button onClick={copy} style={{ background: "transparent", border: `1px solid ${copied ? C.accent3 : "#2a4a6a"}`, color: copied ? C.accent3 : C.muted, cursor: "pointer", fontSize: 11, fontWeight: 700, padding: "3px 10px", borderRadius: 5, fontFamily: "inherit" }}>
          {copied ? "✓ הועתק" : "COPY"}
        </button>
      </div>
      <pre style={{ background: "#060d1a", color: "#cdd9e5", padding: "20px", margin: 0, overflowX: "auto", fontSize: 13, lineHeight: 1.7, fontFamily: "'JetBrains Mono','Fira Code','Courier New',monospace", direction: "ltr", textAlign: "left" }}>
        {code.split('\n').map((line, i) => (
          <div key={i} style={{ display: "flex", gap: 0, minHeight: "1.7em" }}>
            <span style={{ color: "#2a4a6a", userSelect: "none", minWidth: 36, paddingRight: 12, fontSize: 11, lineHeight: 1.7, flexShrink: 0, textAlign: "right" }}>{i + 1}</span>
            <span dangerouslySetInnerHTML={{ __html: highlight(line) || " " }} />
          </div>
        ))}
      </pre>
    </div>
  );
}

